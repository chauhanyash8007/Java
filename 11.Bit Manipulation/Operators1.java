public class Operators1 {

    public static void main(String[] args) {

        // use of Binary &(AND) operator
        System.out.println((5 & 6));

        // use of Binary | (OR) operator
        System.out.println((5 | 6));

        // use of Binary ^ (XOR) operator
        System.out.println((5 ^ 6));

        // use of Binary ~ (Once's (1) complement) operator
        System.out.println((~5));

        // use of Binary Left Shift operator
        System.out.println(5 << 2);

        // use of Binary Right Shift operator
        System.out.println(6 >> 1);
    }
}