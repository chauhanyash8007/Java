// Switch statement

import java.util.*;

public class switch9 {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("somasa");
                break;

            case 2:
                System.out.println("burger");
                break;

            case 3:
                System.out.println("mango shake");
                break;

            default:
                System.out.println("We realize we are dreaming");
        }

    }

}
