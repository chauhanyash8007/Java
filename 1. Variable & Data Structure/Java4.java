// data types 

public class Java4 {
    public static void main(String[] args) {
        byte a = 52;                           // range -128 To 127
        System.out.println(a);

        char ch = 'b';                         // 0 to 65535  or 'a' to 'z' | 'A' to 'Z' | other special symbol like '%' etc.
        System.out.println(ch);

        boolean var = true;                    // only 2 values (true) or (false) 
        System.out.println(var);

        float price = 3.14f;                   // 3.4e-038 to 3.4e+038   |  1.40239846e-45f to 3.40282347e+38f
        System.out.println(price);

        double value = 3.14000;                // 1.7e-308 to 1.7e+308  |  4.94065645841246544e-324 to 1.79769313486231570e+308
        System.out.println(value);                       

        int number = 69;                       // -2147483648 to 2147483647
        System.out.println(number);    
        
        long num = 6284969;                    // -9223372036854775808 to 9223372036854775807
        System.out.println(num);

        short numb = 9999;                     // -32768 to 32767
        System.out.println(numb);


    
    } 
}
