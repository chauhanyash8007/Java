//print pattern

public class for6 {
    public static void main(String[] args) {

        System.out.println("for loop");
        for (int line = 1; line <= 4; line++) {
            System.out.println("* * * *");
        }

        System.out.println("while loop");
        int line = 1;
        while (line <= 4) {
            System.out.println("* * * *");
            line++;
        }
    }

}
