//Assignment Operators

public class Operators5f {
    public static void main(String[] args) {
        int a = 10;
        // a = a + 10;
        a += 10;
        System.out.println(a);

        int b = 10;
        // b = b - 10;
        b -= 10;
        System.out.println(b);

        int c = 10;
        // c = c * 10;
        c *= 10;
        System.out.println(c);

        int d = 10;
        // d = d / 10;
        d /= 10;
        System.out.println(d);

        int e = 10;
        // e = e % 10;
        e %= 10;
        System.out.println(e);
    }

}
