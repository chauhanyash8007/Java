// Logical Operator

public class Operators4d {
    public static void main(String[] args) {
        System.out.println((3 > 2 && 5 > 0));
        System.out.println((3 > 2 && 5 < 0));
        // System.out.println((3<2 && 5>0));
        // System.out.println((3<2 && 5<0));

        // System.out.println((3>2 || 5>0));
        // System.out.println((3>2 || 5<0));
        System.out.println((3 < 2 || 5 > 0));
        System.out.println((3 < 2 || 5 < 0));

        System.out.println(!(3 > 2));
        System.out.println(!(3 < 2));
    }
}
